package serialization;

public class UserGrade {

	private String grade;
	private int point;
	
	public UserGrade() {}

	public String getGrade() {
		return grade;
	}

	public int getPoint() {
		return point;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public void setPoint(int point) {
		this.point = point;
	}
	
	
}
